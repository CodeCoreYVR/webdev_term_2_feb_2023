# Define the number for which you want to find the sine and cosine
number = 45

# Calculate the sine of the number using the Math module
sine = Math.sin(number)

# Calculate the cosine of the number using the Math module
cosine = Math.cos(number)

# Print out the results
puts "The sine of #{number} is #{sine}"
puts "The cosine of #{number} is #{cosine}"


